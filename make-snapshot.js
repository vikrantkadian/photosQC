#!/usr/bin/env node
/*
 * Freeze the EXACT bytes each iOS app receives into ./snapshot/, so static
 * hosting (GitHub Pages etc.) can serve an app-faithful comparison with no
 * server. Fetches every pairs.js URL with that config's REQUEST_HEADERS —
 * the same replay the /img proxy does — and writes the decoded bodies plus
 * a snapshot.js manifest the app loads in static mode.
 *
 *   node make-snapshot.js        (re-run whenever pairs.js changes, then
 *                                 commit snapshot/ and snapshot.js)
 */
'use strict';
const fs = require('fs');
const path = require('path');
const https = require('https');
const zlib = require('zlib');
const vm = require('vm');

const ROOT = __dirname;
const sandbox = { window: {}, location: { hostname: 'make-snapshot' } };
vm.createContext(sandbox);
vm.runInContext(fs.readFileSync(path.join(ROOT, 'pairs.js'), 'utf8'), sandbox);
const PAIRS = Array.isArray(sandbox.window.PAIRS) ? sandbox.window.PAIRS : [];
const HDRS = sandbox.window.REQUEST_HEADERS || {};
const EXT = {
  'image/webp': '.webp', 'image/jpeg': '.jpg', 'image/png': '.png',
  'image/avif': '.avif', 'image/gif': '.gif',
};

if (!PAIRS.length) { console.error('pairs.js has no pairs'); process.exit(1); }
const outDir = path.join(ROOT, 'snapshot');
fs.mkdirSync(outDir, { recursive: true });

function fetch(url, headers, depth) {
  return new Promise(function (resolve, reject) {
    if (depth > 3) return reject(new Error('too many redirects: ' + url));
    let u;
    try { u = new URL(url); } catch (e) { return reject(e); }
    const req = https.get(u, { headers: headers }, function (r) {
      if ([301, 302, 303, 307, 308].indexOf(r.statusCode) >= 0 && r.headers.location) {
        r.resume();
        return resolve(fetch(new URL(r.headers.location, u).href, headers, depth + 1));
      }
      if (r.statusCode !== 200) { r.resume(); return reject(new Error('HTTP ' + r.statusCode + ' for ' + url)); }
      const chunks = [];
      r.on('data', function (c) { chunks.push(c); });
      r.on('end', function () {
        let body = Buffer.concat(chunks);
        const enc = (r.headers['content-encoding'] || '').toLowerCase();
        try {
          if (enc === 'gzip') body = zlib.gunzipSync(body);
          else if (enc === 'br') body = zlib.brotliDecompressSync(body);
          else if (enc === 'deflate') body = zlib.inflateSync(body);
        } catch (e) { return reject(new Error('decode failed for ' + url + ': ' + e.message)); }
        resolve({ body: body, type: (r.headers['content-type'] || '').split(';')[0].trim() });
      });
      r.on('error', reject);
    });
    req.setTimeout(30000, function () { req.destroy(new Error('timeout: ' + url)); });
    req.on('error', reject);
  });
}

(async function () {
  const manifest = [];
  for (let i = 0; i < PAIRS.length; i++) {
    const entry = {};
    for (const cfg of ['config1', 'config2']) {
      const url = PAIRS[i][cfg];
      const got = await fetch(url, HDRS[cfg] || {}, 0);
      const ext = EXT[got.type] || '.bin';
      const name = 'pair' + String(i + 1).padStart(2, '0') + '-' + cfg + ext;
      fs.writeFileSync(path.join(outDir, name), got.body);
      entry[cfg] = 'snapshot/' + name;
      console.log(name + '  ' + got.type + '  ' + got.body.length + ' bytes');
    }
    manifest.push(entry);
  }
  const js = '// GENERATED by make-snapshot.js on ' + new Date().toISOString().slice(0, 10) + ' — do not edit.\n' +
    '// The exact bytes each iOS app received for every pair (fetched with\n' +
    '// REQUEST_HEADERS from pairs.js). In static mode the app shows these,\n' +
    '// so the comparison stays app-faithful without a server.\n' +
    'window.SNAPSHOT_PAIRS = ' + JSON.stringify(manifest, null, 1) + ';\n';
  fs.writeFileSync(path.join(ROOT, 'snapshot.js'), js);
  console.log('Wrote snapshot.js with ' + manifest.length + ' pairs.');
})().catch(function (e) { console.error('FAILED: ' + e.message); process.exit(1); });
